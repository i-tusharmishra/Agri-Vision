  // Helper function to safely get crops
  export const getCropsForState = (stateName) => {
    // Normalize input (trim spaces, handle case sensitivity if needed)
    if (!stateName) return [];
    
    // Try exact match first
    if (stateCropList[stateName]) {
      return stateCropList[stateName];
    }
  
    // Fallback: Try finding a key that includes the state name (e.g. "Punjab" in "Punjab Region")
    const key = Object.keys(stateCropList).find(k => 
      stateName.toLowerCase().includes(k.toLowerCase()) || 
      k.toLowerCase().includes(stateName.toLowerCase())
    );
  
    return key ? stateCropList[key] : [];
  };